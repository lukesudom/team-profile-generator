//push data to HTMl page


const genHTML = (data) => {

    const pageArray = [];
    for (let i = 0; i < data.length; i++) {
        let employee = data[i];
        let role = employee.getRole();


        //if statements for roles


        if (role === 'Manager') {
            let managerCard = genManager(employee);

            pageArray.push(managerCard);
        }

        if (role === 'Engineer') {
            let engineerCard = genEngineer (employee);
            pageArray.push(engineerCard);
        }

        if (role === 'Intern') {
            let internCard = genIntern (employee);
            pageArray.push(internCard);
        }
    }
    
const employeeCards = pageArray.join('');
const generateTeam = generateTeamPage(employeeCards);
return generateTeam;


};





